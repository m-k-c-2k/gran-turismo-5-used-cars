const uint32_t m01[] = {0, 0x9908B0DFU};

static uint32_t mt[624];
static uint32_t mti;

static uint32_t lastSeed;
static uint32_t lastN = 0;

uint32_t mtGen(const uint32_t seed, const uint32_t n) {
    if (seed != lastSeed || lastN >= n) {
        mt[0] = seed;
        for (mti = 1; mti < 624; mti++) {
            mt[mti] = 0x6C078965U * (mt[mti - 1] ^ (mt[mti - 1] >> 30)) + mti;
        }
        lastN = 0;
    }
    uint32_t y;
    while (lastN < n) {
        if (mti >= 624) {
            int j;
            for (j = 0; j < 227; j++) {
                y = (mt[j] & 0x80000000U) | (mt[j + 1] & 0x7FFFFFFFU);
                mt[j] = mt[j + 397] ^ (y >> 1) ^ m01[y & 1];
            }
            for (; j < 623; j++) {
                y = (mt[j] & 0x80000000U) | (mt[j + 1] & 0x7FFFFFFFU);
                mt[j] = mt[j - 227] ^ (y >> 1) ^ m01[y & 1];
            }
            y = (mt[623] & 0x80000000U) | (mt[0] & 0x7FFFFFFFU);
            mt[623] = mt[396] ^ (y >> 1) ^ m01[y & 1];
            mti = 0;
        }
        y = mt[mti++];
        y ^= (y >> 11);
        y ^= (y << 7) & 0x9D2C5680U;
        y ^= (y << 15) & 0xEFC60000U;
        y ^= (y >> 18);
		lastN++;
    }
    lastSeed = seed;
    return y;
}

int idx(const uint32_t s) {
    const int i = (mtGen(s, 1) & 0x7FFFFFFFU) / 189824;
    int low = 0;
    int high = 819;
    while (low <= high) {
        const int mid = low + ((high - low) >> 1);
        if (i < odds[mid]) {
            if (!mid || i >= odds[mid - 1])
                return mid;
            high = mid - 1;
        } else
            low = mid + 1;
    }
    return high + 1;
}

char *printInfo(const uint32_t i) {
    const int index = idx(i);
    const char *brand = brands[brandIds[index]];
    const char *model = mdl[index];
    int colorIndex = 0;
    int j;
    for (j = 0; j < index; j++) {
        colorIndex += numCols[j];
    }
    colorIndex += mtGen(i, 2) % numCols[index];
    const int32_t min = odoMinVers[odoMin[index]];
    const int32_t max = odoMaxVers[odoMax[index]];
    const int32_t odo = odoMax[index] > 0 ? min + mtGen(i, 3) % (max - min) : 0;
    const double num = (double) prices[index] * (1.0 - odo % 100000 / 200000.0);
    const int32_t intNum = (int32_t) num;
    const int32_t price = intNum + (num - intNum > 0.49999);
    const char *color = colors[colorCodes[colorIndex]];
    char *output = malloc(200);
    snprintf(output, 200, "0x%08X\t%s\t%s\t%d\t%d\t%s\n", i, brand, model, odo, price, color);
    return output;
}

